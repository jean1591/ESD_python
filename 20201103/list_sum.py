my_list = [1, 2, 3, 4, 5, 6]


# LIST SUM
# Initialisation à 0 de la variable de total
total_sum = 0

# Parcours des éléments de my_list
for b in my_list:
  # B => Ajout de l'élément parcouru à total
  # print(">>", total)
  total_sum += b

# Affichage de total
print(total_sum)


